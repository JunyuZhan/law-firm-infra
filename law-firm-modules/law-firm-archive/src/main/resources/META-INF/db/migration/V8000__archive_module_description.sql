/*
=============================================================================
                            档案管理模块 - 功能说明
=============================================================================

一、模块职责
档案管理模块负责律师事务所的档案全生命周期管理，包括：
- 档案信息管理
- 档案归档管理
- 档案借阅管理
- 档案分类管理
- 档案存储管理
- 档案销毁管理
- 档案检索管理
- 档案统计分析

二、版本规划 (V8000-V8999)
- V8000: 模块说明文档
- V8001: 表结构定义
- V8002: 基础数据初始化

三、核心功能表
=============================================================================
1. 档案基础管理
   - archive_info: 档案基本信息
   - archive_file: 档案文件管理
   - archive_storage: 档案存储位置

2. 档案分类标签
   - archive_category: 档案分类
   - archive_label: 档案标签
   - archive_label_relation: 档案标签关联

3. 档案业务关联
   - archive_case_relation: 案件档案关联
   - archive_contract_relation: 合同档案关联
   - archive_document_relation: 文档档案关联

4. 档案借阅管理
   - archive_borrow: 档案借阅记录
   - archive_borrow_approval: 借阅审批记录
   - archive_return: 档案归还记录

5. 档案操作记录
   - archive_operation_log: 档案操作日志
   - archive_inventory: 档案盘点记录
   - archive_destroy: 档案销毁记录

四、数据关系
=============================================================================
- archive_info → archive_file (档案文件)
- archive_info → archive_storage (存储位置)
- archive_info → archive_label_relation → archive_label (档案标签)
- archive_info → archive_case_relation (案件关联)
- archive_info → archive_contract_relation (合同关联)
- archive_info → archive_document_relation (文档关联)
- archive_info → archive_borrow (借阅记录)
- archive_borrow → archive_borrow_approval (借阅审批)
- archive_borrow → archive_return (归还记录)
- archive_info → archive_operation_log (操作记录)
- archive_info → archive_inventory (盘点记录)
- archive_info → archive_destroy (销毁记录)

五、业务特点
=============================================================================
1. 生命周期管理
   - 档案从归档到销毁的完整生命周期管理
   - 档案状态流转和追踪
   - 档案保管期限管理

2. 借阅控制
   - 严格的档案借阅审批流程
   - 借阅权限控制和时限管理
   - 借阅记录跟踪和提醒

3. 存储管理
   - 物理存储位置管理
   - 档案编号规则和检索
   - 存储容量和统计分析

4. 安全保障
   - 档案访问权限控制
   - 档案操作日志记录
   - 档案防丢失和损坏管理

六、权限设计
=============================================================================
档案管理功能权限控制：
- 档案查看: ARCHIVE_VIEW
- 档案管理: ARCHIVE_MANAGE
- 档案归档: ARCHIVE_FILE
- 档案借阅: ARCHIVE_BORROW
- 档案审批: ARCHIVE_APPROVE
- 档案盘点: ARCHIVE_INVENTORY
- 档案销毁: ARCHIVE_DESTROY
- 档案统计: ARCHIVE_STATISTICS

七、注意事项
=============================================================================
1. 档案数据涉及机密信息，需严格权限控制
2. 档案借阅需要完善的审批流程和追踪机制
3. 档案存储位置需要准确记录和定期盘点
4. 档案销毁需要符合法律法规要求
5. 档案检索需要支持多维度快速查找
6. 档案统计需要支持多种维度分析
7. 档案备份和容灾需要定期检查

八、存储位置编码规则
=============================================================================
存储位置编码格式：楼层-区域-柜号-层号-位置号
例如：F1-A-001-03-05 表示1楼A区001号柜第3层第5个位置

九、档案编号规则
=============================================================================
档案编号格式：DA-YYYY-类型-流水号
- DA: 档案(Document Archive)前缀
- YYYY: 年份
- 类型: 01-案件档案, 02-合同档案, 03-文档档案, 04-行政档案, 05-财务档案
- 流水号: 4位数字，从0001开始

十、保管期限规则
=============================================================================
- 永久保存: 重要合同、重大案件、公司章程等
- 30年保存: 一般合同、重要文件等
- 10年保存: 财务账册、审计报告等
- 5年保存: 一般案件材料、行政文件等
- 3年保存: 临时文件、通知公告等

=============================================================================
