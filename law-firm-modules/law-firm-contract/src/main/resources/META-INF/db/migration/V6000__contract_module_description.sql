=============================================================================
1. 合同基础管理
   - contract_info: 合同基本信息
   - contract_attachment: 合同附件
   - contract_clause: 合同条款

2. 合同团队费用
   - contract_team: 合同团队
   - contract_fee: 合同费用

3. 合同审核流程
   - contract_review: 合同审核
   - contract_approval: 合同审批
   - contract_milestone: 合同里程碑

4. 合同模板变更
   - contract_template: 合同模板
   - contract_change: 合同变更
   - contract_conflict: 合同冲突

四、数据关系
=============================================================================
- contract_info → contract_attachment (合同附件)
- contract_info → contract_clause (合同条款)
- contract_info → contract_team (合同团队)
- contract_info → contract_fee (合同费用)
- contract_info → contract_review (合同审核)
- contract_info → contract_approval (合同审批)
- contract_info → contract_milestone (合同里程碑)
- contract_info → contract_change (合同变更)
- contract_template → contract_info (基于模板创建)

五、业务特点
=============================================================================
1. 全生命周期管理
   - 合同创建、审核、签署、履行、变更、终止
   - 完整的流程管控和状态跟踪

2. 多维度管理
   - 按客户、案件、类型、状态等多维度管理
   - 支持复杂的费用结构和团队协作

3. 智能化功能
   - 合同模板管理和自动生成
   - 冲突检查和风险预警
   - 里程碑管理和提醒

六、权限设计
=============================================================================
合同管理功能权限控制：
- 合同查看: CONTRACT_VIEW
- 合同管理: CONTRACT_MANAGE
- 合同审核: CONTRACT_REVIEW
- 合同审批: CONTRACT_APPROVE
- 合同模板管理: CONTRACT_TEMPLATE_MANAGE
- 合同费用管理: CONTRACT_FEE_MANAGE

七、注意事项
=============================================================================
1. 合同信息涉及商业机密，需严格权限控制
2. 合同变更需要完整的审批流程和版本管理
3. 费用信息需要与财务系统集成
4. 合同期限和里程碑需要定期提醒
5. 合同模板需要法务团队定期维护更新

