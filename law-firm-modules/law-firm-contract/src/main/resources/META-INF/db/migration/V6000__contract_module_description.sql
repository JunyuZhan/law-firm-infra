一、模块职责
合同管理模块负责律师事务所的合同全生命周期管理，包括：
- 合同基础信息管理
- 合同条款管理
- 合同团队管理
- 合同费用管理
- 合同审核审批
- 合同模板管理
- 合同变更管理
- 合同冲突检查

