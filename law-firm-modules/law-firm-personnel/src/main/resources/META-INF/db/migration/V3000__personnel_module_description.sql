=============================================================================
                            人员管理模块 - 功能说明
=============================================================================

一、模块职责
人员管理模块负责律师事务所的人员信息管理，包括：
- 人员基础信息管理
- 律师专业信息管理
- 员工工作信息管理
- 客户关系管理
- 联系人信息管理
- 人员档案管理

二、版本规划 (V3000-V3999)
- V3000: 模块说明文档
- V3001: 表结构定义
- V3002: 基础数据初始化

三、核心功能表
=============================================================================
1. 人员基础信息
   - per_person: 人员基本信息
   - per_contact: 联系方式信息
   - per_address: 地址信息

2. 律师管理
   - per_lawyer: 律师专业信息
   - per_education_experience: 教育经历
   - per_work_experience: 工作经历

3. 员工管理
   - per_employee: 员工信息
   - per_contract: 合同信息
   - per_employee_organization: 员工组织关系

4. 客户管理
   - per_client: 客户信息
   - per_client_contact: 客户联系人
   - per_client_relationship: 客户关系

四、数据关系
=============================================================================
- per_person → per_contact (人员联系方式)
- per_person → per_address (人员地址信息)
- per_person → per_lawyer (律师专业信息)
- per_person → per_employee (员工工作信息)
- per_lawyer → per_education_experience (教育经历)
- per_lawyer → per_work_experience (工作经历)
- per_employee → per_contract (劳动合同)
- per_employee → per_employee_organization (组织关系)

五、业务特点
=============================================================================
1. 数据结构规范化
   - 将原有庞大的per_employee表(56字段)拆分为9个逻辑表
   - 实现数据结构清晰，便于维护和扩展

2. 多角色支持
   - 同一人员可同时具备多种身份(律师+员工)
   - 灵活的身份转换和权限管理

3. 完整档案管理
   - 详细的教育和工作经历记录
   - 完整的联系方式和地址管理

六、权限设计
=============================================================================
人员管理功能权限控制：
- 人员查看: PERSONNEL_VIEW
- 人员管理: PERSONNEL_MANAGE
- 律师管理: LAWYER_MANAGE
- 员工管理: EMPLOYEE_MANAGE
- 客户管理: CLIENT_MANAGE

七、注意事项
=============================================================================
1. 人员信息涉及隐私，需严格权限控制
2. 律师执业信息需要定期更新和验证
3. 客户信息需要做好数据保护和备份
4. 人员档案变更需要留存历史记录

