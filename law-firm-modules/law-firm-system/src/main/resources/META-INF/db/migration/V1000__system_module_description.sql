/*
=============================================================================
                            系统管理模块 - 功能说明
=============================================================================

一、模块职责
系统管理模块负责系统的基础管理功能，包括：
- 菜单权限管理
- 角色权限分配  
- 组织架构管理
- 定时任务管理
- 系统监控功能
- 系统升级管理

二、版本规划 (V1000-V1999)
- V1000: 模块说明文档
- V1001: 表结构定义
- V1002: 基础数据初始化

三、核心功能表
=============================================================================
1. 菜单管理
   - sys_menu: 系统菜单结构

2. 角色管理  
   - sys_role: 角色信息
   - sys_role_menu: 角色菜单关联

3. 组织管理
   - sys_organization: 组织架构
   - sys_department: 部门信息

4. 定时任务
   - sys_job: 定时任务配置
   - sys_job_log: 任务执行日志

5. 系统监控
   - sys_monitor: 系统监控配置
   - sys_monitor_log: 监控日志

6. 系统升级
   - sys_upgrade: 升级记录
   - sys_upgrade_log: 升级日志

四、数据关系
=============================================================================
- sys_role → sys_role_menu → sys_menu (角色菜单权限)
- sys_organization → sys_department (组织部门层级)
- sys_job → sys_job_log (任务执行记录)
- sys_monitor → sys_monitor_log (监控记录)

五、权限设计
=============================================================================
系统管理功能需要超级管理员权限：
- 菜单管理: MENU_MANAGE
- 角色管理: ROLE_MANAGE  
- 组织管理: ORG_MANAGE
- 任务管理: JOB_MANAGE
- 监控管理: MONITOR_MANAGE
- 升级管理: UPGRADE_MANAGE

六、注意事项
=============================================================================
1. 系统模块的权限数据已移至认证模块(V2000+)管理
2. 本模块专注于系统管理功能的表结构和基础数据
3. 监控和升级功能为系统核心功能，需谨慎操作
4. 定时任务配置需要系统重启后生效

