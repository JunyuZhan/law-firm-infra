/*
=============================================================================
                            案件管理模块 - 功能说明
=============================================================================

一、模块职责
案件管理模块负责律师事务所的案件业务管理，包括：
- 案件基础信息管理
- 案件详细信息管理
- 案件参与人管理
- 案件团队管理
- 案件费用管理
- 案件文档管理
- 案件任务管理
- 案件事件管理

二、版本规划 (V4000-V4999)
- V4000: 模块说明文档
- V4001: 表结构定义
- V4002: 基础数据初始化

三、核心功能表
=============================================================================
1. 案件基础信息
   - case_info: 案件基本信息(简化核心字段)
   - case_detail: 案件详细信息
   - case_fee: 案件费用信息

2. 案件团队管理
   - case_team: 案件团队配置
   - case_participant: 案件参与人

3. 案件执行管理
   - case_task: 案件任务
   - case_document: 案件文档
   - case_event: 案件事件记录

四、数据关系
=============================================================================
- case_info → case_detail (案件详细信息)
- case_info → case_fee (案件费用)
- case_info → case_team (案件团队)
- case_info → case_participant (案件参与人)
- case_info → case_task (案件任务)
- case_info → case_document (案件文档)
- case_info → case_event (案件事件)

五、业务特点
=============================================================================
1. 数据结构规范化
   - 将原有庞大的case_info表(55+字段)拆分为8个逻辑表
   - 实现案件信息结构化管理

2. 完整业务流程
   - 从案件接收到结案的全流程管理
   - 详细的案件进展和状态跟踪

3. 多维度管理
   - 支持案件分类、优先级、状态管理
   - 灵活的团队配置和权限控制

六、权限设计
=============================================================================
案件管理功能权限控制：
- 案件查看: CASE_VIEW
- 案件管理: CASE_MANAGE
- 案件分配: CASE_ASSIGN
- 费用管理: CASE_FEE_MANAGE
- 文档管理: CASE_DOC_MANAGE
- 任务管理: CASE_TASK_MANAGE

七、状态流转
=============================================================================
案件状态流转：
1. 待接收 → 2. 进行中 → 3. 已完成 → 4. 已关闭
           ↓
        5. 已暂停 → 2. 进行中

八、注意事项
=============================================================================
1. 案件信息涉及法律机密，需严格保密管理
2. 案件状态变更需要记录完整的操作日志
3. 案件文档需要版本控制和权限管理
4. 费用信息需要准确核算和定期对账

